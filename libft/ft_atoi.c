#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	unsigned long	number;
	int				sign;

	i = 0;
	number = 0;
	sign = 1;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		number = number * 10 + str[i] - '0';
		if (number >= LLONG_MAX && sign == -1)
			return (0);
		if (number >= LLONG_MAX)
			return (-1);
		i++;
	}
	return (number * sign);
}
